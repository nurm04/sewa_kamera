<p style="font-size: 30px; font-weight: 600;" class="mt-3">Panduan Penyewaan Kamera</p>
<ol>
    <li><strong>Registrasi / Login:</strong> Buat akun atau masuk terlebih dahulu.</li>
    <li><strong>Pilih Kamera:</strong> Buka menu <i>Kamera</i>, klik salah satu kamera untuk lihat detail dan spesifikasi.</li>
    <li><strong>Lakukan Penyewaan:</strong> Tentukan tanggal pinjam dan kembali, serta jumlah unit.</li>
    <li><strong>Konfirmasi & Pembayaran:</strong> Cek detail penyewaan dan lakukan pembayaran sesuai metode yang tersedia.</li>
    <li><strong>Pengambilan / Pengiriman:</strong> Kamera akan disiapkan untuk diambil atau dikirim ke alamatmu.</li>
    <li><strong>Pengembalian:</strong> Kembalikan kamera tepat waktu untuk menghindari denda keterlambatan.</li>
</ol>
<p><strong>Catatan:</strong> Denda dikenakan jika ada keterlambatan atau kerusakan pada kamera.</p>
